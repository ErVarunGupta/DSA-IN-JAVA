package BitManipulation;

public class BitwiseOperator {
    public static void main(String[] args) {
        //bitwise AND
        System.out.println(5&6);
        //bitwise OR
        System.out.println(5|6);
        //bitwise XOR
        System.out.println(5^6);
        //bitwise One's complement
        System.out.println(~0);
        //Left shift
        System.out.println(5<<2);
        //Right shift
        System.out.println(5>>2);
    }
}
